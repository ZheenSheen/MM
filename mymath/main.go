package mymath

import "math"

func Abs(x float64) float64 {
	return math.Abs(x)
}

func Acos(x float64) float64 {
	return math.Acos(x)
}

func Acosh(x float64) float64 {
	return math.Acosh(x)
}

func Asin(x float64) float64 {
	return math.Asin(x)
}

func Max(a, b float64) float64 {
	return math.Max(a,b)
}

func Sqrt(x float64) float64 {
	return math.Sqrt(x)
}

func Yn(y, n float64) float64 {
	return math.Yn(y, n)
}
